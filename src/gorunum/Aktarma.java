    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
         Malzeme seciliMalzeme=(Malzeme)jComboBox3.getSelectedItem();
        
        if(seciliMalzeme==null){
            return;
            
        }
        
       int temp=seciliMalzeme.getSayisi();
       int cikacak=Integer.parseInt(jTextField1.getText().toString());
       int sonuc=temp-cikacak;
       seciliMalzeme.setSayisi(sonuc);
       Veritabani.merge(seciliMalzeme);
       seciliDepo2=(Depo)jComboBox2.getSelectedItem();
       Malzeme yeniMalzeme=new Malzeme();
       yeniMalzeme.setDepo(seciliDepo2);
       yeniMalzeme.setTur(seciliMalzeme.getTur());
       yeniMalzeme.setSayisi(cikacak);
       Veritabani
               .persist(yeniMalzeme);
       veriCek();
    }//GEN-LAST:event_jButton1ActionPerformed

