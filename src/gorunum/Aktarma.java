        jLabel8.setText("");
        
        if(seciliDepo!=null){
            for(Malzeme m:malzemeList){
               if(m.getDepo().getId()==seciliDepo.getId()){
                    jComboBox3.addItem(m);
                    
               }
            }
         jButton1.setVisible(true);
        jTextField1.setVisible(true);
        jLabel4.setVisible(true);
        }
      
      
