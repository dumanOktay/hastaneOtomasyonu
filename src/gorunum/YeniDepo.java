    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
       str=(String)jComboBox1.getSelectedItem();
       
       if(str==null){
           return;
       }
       
       if(str.equals("Servis")){
           List<Servis> servisList= Veritabani.readObject(new Servis());
           jComboBox2.removeAllItems();
           
           for(Servis s:servisList){
               jComboBox2.addItem(s);
           }
       }
       else{
           jComboBox2.removeAllItems();
           
           List<Poliklinik> polikliniklList=Veritabani.readObject(new Poliklinik());
           for(Poliklinik p: polikliniklList){
               jComboBox2.addItem(p);
           }
       }
    }//GEN-LAST:event_jComboBox1ActionPerformed

