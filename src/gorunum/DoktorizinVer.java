    public void tarihEkle(){
        String[] aylar= new DateFormatSymbols(Locale.getDefault()).getMonths();
        
        ay.removeAllItems();
        
        for(String s:aylar){
            ay.addItem(s);
        }
        
        gun.removeAllItems();
        
        for(int i=1;i<31;i++){
            gun.addItem(i);
        }
        
         for (int saat = 9; saat < 17; saat++) {
            if (saat == 12) {
                continue;
            }
            for (int dakka = 0; dakka < 12; dakka++) {
                String saatStr = String.format("%02d:%02d", saat, dakka * 5);
                saatList.add(saatStr);
            }

        }
}

