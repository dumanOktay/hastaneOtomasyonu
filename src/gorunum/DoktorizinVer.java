    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       seciliDoktor=(Doktor)jComboBox2.getSelectedItem();
       
       if(seciliDoktor==null){
           return;
       }
       
        String seciliString=(String)jComboBox1.getSelectedItem();
        if(seciliString==null){
            return;
        }
       
        Izin izin= new Izin();
        izin.setAy((String)ay.getSelectedItem());
        izin.setDoktor(seciliDoktor);
        izin.setGun((Integer)gun.getSelectedItem());
        if(seciliString.equals("Gunluk")){
            izin.setIsGunluk(true);
        }else{
            izin.setIsGunluk(false);
            izin.setSaat((String)saatCombo.getSelectedItem());
        }
        Veritabani.persist(izin);
        Veritabani.merge(seciliDoktor);
    }//GEN-LAST:event_jButton1ActionPerformed

